package module2;

public class Level2_Arithmetic_Operators {

	public static void main(String[] args) {
		 int a = 10; int b = 15;
		    int c = a + b;
		    System.out.println("Value of 'a' after '+' Arithmetic operation is " + c);
		    c= b-a;
		    System.out.println("Value of 'a' after '-' Arithmetic operation is " + c);
		    c=b*a ;
		    System.out.println("Value of 'a' after '*' Arithmetic operation is " + c);
		    c=b/a;
		    System.out.println("Value of 'a' after '/' Arithmetic operation is " + c);
		    c=b%a;
		    System.out.println("Value of 'a' after '%' Arithmetic operation is " + c);
		    a++;
		    System.out.println("Value of 'b' after '++' Arithmetic operation is " + a);
		    b--;
		    System.out.println("Value of 'c' after '--' Arithmetic operation is " + b);

	}

}
